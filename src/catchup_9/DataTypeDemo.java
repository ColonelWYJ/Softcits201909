package catchup_9;

public class DataTypeDemo {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 100;
		
		int result = a / b;
		double result_1 = a / b;
		double result_2 = a * (double)1.0 / b;
		
		System.out.println(result);
		
		System.out.println(result_1);
		
		System.out.println(result_2);

	}

}
